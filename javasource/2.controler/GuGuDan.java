public class GuGuDan{
	public static void main(String[] args){ 
		System.out.println("***备备窜1***");
		for(int i=1;i<10;i++){
			for(int dan=2;dan<10;dan++){
				System.out.print(dan + " * " + i + " = " + dan * i + "  ");
			}
			System.out.println();
		}
		
		System.out.println("\n***备备窜2***");
		
		for(int dan=1;dan<10;dan++){
			for(int i=1;i<10;i++){
				System.out.print(dan + " * " + i + " = " + dan * i + "  ");
			}
			System.out.println();
		}
	
	}
}

